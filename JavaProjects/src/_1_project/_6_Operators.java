package _1_project;

public class _6_Operators {

	public static void main(String[] args) {
		double number1 = 8.4, number2 = 4.2, result = 0.0;
		
		result = number1 + number2;
		System.out.println("number1 + number2: " + result);
		
		
		result = number1 - number2;
		System.out.println("number1 - number2: " + result);
		
		result = number1 * number2;
		System.out.println("number1 * number2: " + result);
		
		result = number1 / number2;
		System.out.println("number1 / number2: " + result);
		
		result = number1 % number2;
		System.out.println("number1 % number2: " + result);
		
		
	}

}
